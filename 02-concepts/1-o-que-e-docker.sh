    1)História Docker:
        - Através da empresa dot cloud, Solomon Hykes em 2008 começou a criar containers que em 2013 seria o surgimento do docker.
        - 2013: O projeto dot cloud virou opensource e foi chamado de docker, começando a participação da comunidade internacional em seu desenvolvimento.

    2)O que é docker?
        - É um container engine opensource criado em 2013 junto a comunidade opensource.
        - Um software que fornece containers virtuais, impacota sua aplicação e suas dependencias para dentro de um container.
          A partir desse momento, o container se torna portável para ser utilizado em qualquer outro lugar/compuutador.
