                        IMAGE vs CONTAINER
                        ------------------

1)Image:
    -> Imagem é a aplicação que queremos rodar
    -> Podemos ter vários containers rodando a partir da mesma imagem

2)Container:
    -> Container é a instância da imagem rodando como um processo
    -> Podemos ter container de imagens como:
        - ubuntu-image
        - postgres-image
        - etc


